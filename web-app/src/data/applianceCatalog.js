// Cihaz adi kullanici tarafindan ozellestirilebildigi icin (ör. "Televizyon Salon"),
// ikonu tam esitlik yerine ad icinde gecen bilinen turu arayarak buluyoruz.
export function getApplianceIcon(applianceName) {
  if (!applianceName) {
    return "🔌";
  }

  const lowerName = applianceName.toLocaleLowerCase("tr-TR");

  const matchedType = Object.keys(applianceIcons).find((type) =>
    lowerName.includes(type.toLocaleLowerCase("tr-TR"))
  );

  return matchedType ? applianceIcons[matchedType] : "🔌";
}

